<?php namespace Fut7\Application\Season\CRUD\Create;


class CreateSeasonCommand
{
    private string $id;
    private string $name;

    public function __construct($id, $name)
    {
        $this->id = $id;
        $this->name = $name;
    }

    public function id(): string
    {
        return $this->id;
    }

    public function name(): string
    {
        return $this->name;
    }
}